    private void UpdateTable(){
        model.getDataVector().removeAllElements();
        conn = javaconnect.ConnectDb();
        String sql_query = ("SELECT * FROM utilisateur");
        try{
            pst = conn.prepareStatement(sql_query);
            Rs = pst.executeQuery();
            while(Rs.next()){
                model.addRow(new Object[]{Rs.getString("user"), /*Rs.getString("pass"),*/ Rs.getString("libuser"),
                    Rs.getString("code_fact"), /*Rs.getString("image"), Rs.getString("opti")*/});
                }
            }
            catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
            }
            table_utilisateur.setModel(model);
    }
    
