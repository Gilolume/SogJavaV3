    public void ajouter_utilisateur(){
        if ("".equals(txt_user.getText())){
            JOptionPane.showMessageDialog(null, "Le champ utilisateur est obligatoire.");
            txt_user.requestFocus();
        }
        else{
            try{
                conn = javaconnect.ConnectDb();
                String sql_user_exist_verif = ("SELECT user FROM utilisateur WHERE user = '"+txt_user.getText()+"';");
                pst = conn.prepareStatement(sql_user_exist_verif);
                Rs = pst.executeQuery();
                if (Rs.next()){
                    JOptionPane.showMessageDialog(null, "Ce nom d'utilisateur existe déjà.");
                    txt_user.requestFocus();
                }
                else{
                    if (txt_pass.getText().equals(txt_pass_confirmation.getText())){
                        int option = JOptionPane.showConfirmDialog(null, "Confirmer l'ajout de l'utilisateur.");
                        if(option == JOptionPane.OK_OPTION){
                            String sql_ajout_utilisateur = ("INSERT INTO utilisateur VALUES('"+txt_user.getText().replace("'", "''")+"','"+txt_pass.getText().replace("'", "''")+"','"+txt_libuser.getText().replace("'", "''")+
                                "','"+txt_code_fact.getText().replace("'", "''")+"','"+image_par_defaut+"','"+txt_opti.getText().replace("'", "''")+"');");
                            pst = conn.prepareStatement(sql_ajout_utilisateur);
                            pst.executeUpdate();
                            JOptionPane.showMessageDialog(null, "Utilisateur ajouté avec succès.");
                            UpdateTable();
                        }
                    }
                    else{
                        JOptionPane.showMessageDialog(null, "Confirmation du mot de passe incorrecte.");
                        txt_pass.setText("");
                        txt_pass_confirmation.setText("");
                        txt_pass.requestFocus();
                    }
                }  
            }
            catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }
    
