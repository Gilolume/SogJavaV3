    public void supprimer_utilisateur(){
        if ("".equals(txt_user.getText())){
            JOptionPane.showMessageDialog(null, "Aucun utilisateur sélectionner.");
        }
        else{
            if (Login_jframe.getUtilisateur_conn().equals(txt_user.getText())){
                JOptionPane.showMessageDialog(null, "Vous ne pouvez pas vous supprimer.");
            }
            else{
                int option = JOptionPane.showConfirmDialog(null, "Confirmer la suppression de l'utilisateur.");
                if(option == JOptionPane.OK_OPTION){
                    try{
                        conn = javaconnect.ConnectDb();
                        String sql_ajout_utilisateur = ("DELETE FROM utilisateur WHERE user = '"+txt_user.getText()+"';");
                        pst = conn.prepareStatement(sql_ajout_utilisateur);
                        pst.executeUpdate();
                        JOptionPane.showMessageDialog(null, "Utilisateur supprimé avec succès.");
                        UpdateTable();
                    }
                    catch(Exception e){
                            JOptionPane.showMessageDialog(null, e);
                    }
                }
            }
        }
    }
    
